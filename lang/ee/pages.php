<h2>IX. Kaubaga seotud kaebused</h2>

<p><strong>Kehtib kliendile, kes on tarbija:</strong></p>

<p>1. Kõik Kaupluses pakutavad kaubad on täiesti uued, juriidiliste defektideta ja seaduslikult Ühendkuningriigi turule toodud. Müüjal on seadusest tulenev kohustus tarnida kaubad puudusteta. Müüja vastutust seaduses sätestatud teenuse kvaliteedi eest reguleerivad kauba lepingule mittevastavuse üldkohaldatavad sätted, eelkõige 30. mai 2014. a tarbijaõiguste seaduse peatükk 5a.</p>

<p>2. Kauba lepingutingimustele mittevastavuse korral võib Klient esitada Müüjale kaebuse mis tahes vormis, eelkõige kirjalikult järgmisel aadressil: FORAM spółka z ograniczoną odpowiedzialnością sp. k., ul. Jeziorna 3, 64-200, Karpicko või e-posti teel järgmisele aadressile: <a href="mailto: b2b@artforma.eu">b2b@artforma.eu</a>. Kaebuses on soovitatav:</p>

<ul>
    <li>teabe esitamine kaebuse eseme kohta, eelkõige defekti tüüp ja kuupäev.</li>
    <li>päringu määratlemine puuduse kõrvaldamise viisi kohta (kauba asendamine uuega, kauba parandamine ja seejärel hinna alandamine või lepingust taganemine)</li>
    <li>kaebuse esitaja kontaktandmete esitamine</li>
</ul>

<p>mis hõlbustab ja kiirendab kaebuse läbivaatamist poes. Eelmises lauses toodud soovitused on ainult mittesiduvad juhised ega mõjuta mingil juhul ilma soovitatud teavet esitamata esitatud kaebuste tõhusust.</p>

<p>3. Pretensiooni läbivaatamise vajaduse korral annab Klient Müüjale kaebuse esemeks oleva kauba, mis kuulub parandamisele või väljavahetamisele. Müüja tuleb kaubale kliendilt omal kulul järgi. Kui kaup paigaldati enne kauba lepingule mittevastavuse ilmnemist, võtab Müüja kauba lahti ja monteerib pärast parandamist või asendamist uuesti või laseb need toimingud teha omal kulul.</p>

<p>4. Kaebuse läbivaatamise kiirendamiseks võib Klient võimaldada kaebuse läbivaatamist distantsilt, eelkõige fotode esitamise või videokonverentsi režiimis tutvumise võimaldamise kaudu. Ebamugavuste vältimiseks võib Klient kasutada kaebusega distantsilt läbivaatamise võimalust ka juhul, kui toote kohaletoimetamine on võimalik, selline otsus ei piira Kliendi õigusi.</p>

<p>5. Müüja on kohustatud tarbija pretensioonile vastama 14 päeva jooksul alates selle kättesaamisest. Kauba asendamise, puuduste kõrvaldamise või lepingust taganemise avalduse esitamisel lähtudes kauba lepingule mittevastavuse sätetest (30. mai 2014. a tarbijaõiguste seaduse peatükk 5a), on Müüja kohustatud kauba parandama või vahetama ostja.

<p>6. Kui Müüja peab kaebust põhjendamatuks, toimetatakse kaup Kliendile koos vastava teabega.</p>

<p>7. Kaubale järeletulemata jätmise korral on Kliendil võimalik kaubale ise järgi tulla Müüja laost talle sobival ajal (tööpäeval kell 9-15) pärast Müüja teavitamist valitud kuupäevast. Müüja ja Kliendi vahelisel eraldi kokkuleppel on võimalik kauba edasitoimetamine Müüja poolt või kohaletoimetamine teisele märgitud aadressile.

<p>8. Müüja vastutab kauba üleandmise hetkel eksisteerinud lepingule mittevastavuse eest, mis ilmnes kahe aasta jooksul sellest hetkest arvates. Kauba lepingule mittevastavust, mis ilmnes kahe aasta jooksul kauba üleandmise hetkest, eeldatakse, et see oli olemas üleandmise hetkel, kui ei ole tõendatud vastupidist või kui eeldust ei ole võimalik ühildada kauba olemusega või kauba lepingule mittevastavuse olemusega. .</p>

<p>9. Mõne Kauba puhul annab Müüja lisagarantii garantiidokumendis sätestatud tingimustel. Müüja poolt antud garantii ei välista, piira ega peata Tarbijast Kliendi õigusi vastavalt kauba lepingule mittevastavuse sätetele.</p>

<p><strong>Kehtib mittetarbijast kliendile:</strong></p>

<p>1. Klientide puhul, kes ei ole Tarbijad käesolevate eeskirjade punkti I alajao 2 tähenduses, lähtudes art. TsK 558 § 1 kohaselt välistavad pooled Müüja garantiijärgse vastutuse müüdud kauba puuduste eest.</p>

<p>2. Eelmises punktis nimetatud garantii ainuvastutus ei välista müüjat valitud Kaubale garantii andmist või individuaalsete läbirääkimiste käigus Kliendiga, kes ei ole Tarbija.</p>

