<p><i>Müüja teatab, et suurem osa Veebipoes pakutavatest Kaupadest on täielikult valmistatud Kliendi individuaalse tellimuse järgi ja kohandatud vastavalt tema vajadustele. Seetõttu on suurem osa Veebipoes pakutavatest Kaupadest kokkupandavad kaubad, mis on valmistatud vastavalt tarbija spetsifikatsioonidele või teenindavad tema individuaalseid vajadusi. Vastavalt Art. Tarbijaõiguste seaduse 38 punkti 3 kohaselt ei ole tarbijal õigust kauglepingust taganeda lepingute puhul, mille esemeks on kõnealune kaup.<br />
        Eelnevat silmas pidades märgib Müüja, et allpool kirjeldatud kauglepingust taganemise õigus kehtib ainult <strong>Valentina kollektsiooni toodetele (mööblikomplektid)</strong>. </i></p>

<p>1. Tarbijast kliendil on 30. mai 2014. a tarbijaõiguste seaduses sätestatu kohaselt õigus põhjust avaldamata lepingust taganeda neljateistkümne päeva jooksul. Neljateistkümnepäevane tähtaeg algab kauba valdusesse võtmisest Kliendi või tema poolt märgitud kolmanda isiku, kes ei ole vedaja, poolt ning mitme kauba puhul, mis tarnitakse eraldi, partiidena või osadena, viimase kauba, selle partii või osa valdusesse võtmisest. Tähtajast kinnipidamiseks piisab avalduse saatmisest enne selle aegumist. Lepingust taganemise avalduse saab esitada mis tahes viisil, näiteks:</p> kaudu

<ul>
    <li>traditsiooniline post: FORAM spółka z ograniczoną odpowiedzialnością sp. k., ul. Jeziorna 3, 64-200 Karpicko.</li>
    <li>e-post: <a href="mailto: b2b@artforma.eu">b2b@artforma.eu</a></li>
    <li>telefoni teel: <a href="tel: +442038085213">+44 203 808 5213</a>.</li>
</ul>

<p>2. Klient võib esitada ka lepingust taganemise avalduse, kasutades 30. mai 2014. a tarbijaõiguste seaduse lisas 2 sisalduvat vormi, mille mall on toodud ka allpool Tingimustes. See mall tarnitakse lisaks püsival andmekandjal koos kauglepingu sõlmimise kinnitusega.</p>

<p>3. Kliendi poolt lepingust taganemise korral tagastab Müüja viivitamata – kuid mitte hiljem kui neljateistkümne päeva jooksul alates Kliendi lepingust taganemise avalduse kättesaamise päevast – Kliendile kõik tema tehtud maksed, sealhulgas kauba kohaletoimetamise kulud.</p>

<p>4. Kui Klient on valinud kauba kättetoimetamiseks müüja poolt pakutavast odavaimast tavapärasest tarneviisist erineva kauba kättetoimetamise viisi, ei ole müüja kohustatud Kliendile hüvitama tema poolt tekitatud lisakulusid, mis ületavad Müüja poolt pakutud odavaima tavapärase kättetoimetamise viisi kulusid.</p>

<p>5. Müüja tagastab makse, kasutades sama makseviisi, mida kasutas klient.</p>

<p>6. Müüja võib kliendilt saadud maksete hüvitamist kinni pidada kuni kauba tagasisaamiseni või kuni klient esitab tõendi selle tagastamise kohta, olenevalt sellest, milline sündmus toimub enne.</p>

<p>7. Lepingust taganemise korral on Klient kohustatud viivitamata, kuni neljateistkümne päeva jooksul, tagastama ostetud kauba täies mahus (koos muuhulgas kasutusjuhendi ja muude dokumentidega) seisukorras, mis ei ületa selle kasutusviisi, mis on vajalik asjade olemuse, omaduste ja toimimise kindlakstegemiseks. Kauba tagastamise otsesed kulud kannab klient. Tähtajast kinnipidamiseks piisab kauba tagasisaatmisest enne selle kehtivusaja lõppu. Soovitatav on selle aja jooksul ostetud kaup tagasi saata järgmisele aadressile: FORAM spółka z ograniczoną odpowiedzialnością sp. k., ul. Jeziorna 3, 64-200 Karpicko.</p>

<p>8. Klient vastutab kauba väärtuse vähenemise eest, mis tuleneb kauba kasutamisest viisil, mis läheb kaugemale sellest, mis on vajalik kauba olemuse, omaduste ja toimimise kindlakstegemiseks, välja arvatud juhul, kui Müüja ei ole teavitanud tarbijat lepingu sõlmimise õigusest vastavalt artikli nõuetele. 12 sek. 30. mai 2014. aasta tarbijaõiguste seaduse 1 punkt 9. Tulenevalt transpordi teemast Müüja soovitab, kuid ei nõua kauba tagastamisega saatekindlustust.</p>

<p>9. Tarbijal ei ole õigust kauglepingust taganeda järgmiste lepingute puhul:</p>

<ul>
    <li>teenuse osutamiseks, kui ettevõtja on teenuse täielikult täitnud tarbija selgesõnalisel nõusolekul, keda teavitati enne teenuse osutamise algust, et pärast ettevõtjapoolset teenuse osutamist kaotab ta taganemisõiguse.</li>
    <li>mille puhul hind või tasu sõltub finantsturu kõikumisest, mille üle ettevõtjal puudub kontroll ja mis võivad ilmneda enne tarbija poolt tellitud või tema isikuga lähedalt seotud kinnisvaraga seotud teenuste lepingust taganemise tähtaja möödumist.</li>
    <li><strong>milles teenuse objektiks on mittemonteeritav toode, mis on valmistatud vastavalt tarbija spetsifikatsioonidele või teenindab tema individuaalseid vajadusi.</strong></li>
    <li>mille puhul teenuse teemaks on kiiresti riknevad või lühikese säilivusajaga kaubad.</li>
    <li>milles teenuse esemeks on kinnises pakendis tarnitud kaup, mida ei saa pärast pakendi avamist tervisekaitse- või hügieenikaalutlustel tagastada, kui pakend avati pärast kohaletoimetamist.</li>
    <li>mille puhul teenuse objektiks on kaubad, mis oma olemuse tõttu on pärast kohaletoimetamist lahutamatult seotud teiste esemetega.</li>
    <li>milles teenuse esemeks on alkohoolsed joogid, mille hind lepiti kokku müügilepingu sõlmimisel ja mille tarnimine võib toimuda alles 30 päeva pärast ning mille väärtus sõltub turukõikumistest, mille üle ettevõtjal puudub kontroll.</li>
    <li>milles tarbija nõudis selgesõnaliselt, et ettevõtja tuleks tema juurde kiireloomuliseks remondiks või hoolduseks; kui ettevõtja osutab muid lisateenuseid kui tarbija tellitud või tarnib remondiks või hoolduseks vajalikke esemeid peale varuosade, on tarbijal õigus lepingust taganeda lisateenuste või -esemete osas.</li>
    <li>milles teenuse objektiks on heli- või visuaalsalvestised või arvutiprogrammid, mis tarnitakse suletud pakendis, kui pakend avati pärast kohaletoimetamist.</li>
    <li>ajalehtede, perioodiliste väljaannete või ajakirjade kohaletoimetamiseks, välja arvatud tellimisleping.</li>
    <li>lõpetatud avaliku oksjoniga.</li>
    <li>majutusteenuste osutamiseks, välja arvatud elamiseks, kaubaveoks, auto rentimiseks, gastronoomiaks, vaba aja veetmise, meelelahutuse, spordi- või kultuuriüritustega seotud teenuste osutamiseks, kui lepingus on märgitud teenuse osutamise päev või periood.</li>
    <li>materiaalsele andmekandjale salvestamata digitaalse sisu edastamiseks, kui teenuse osutamine algas tarbija selgesõnalisel nõusolekul enne lepingust taganemise tähtaega ja pärast ettevõtja teavitamist lepingust taganemise õiguse kaotamisest.</li>
</ul>

