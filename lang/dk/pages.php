<h2>IX. Klager vedrørende varer</h2>

<p><strong>Gælder for Kunden, der er Forbruger:</strong></p>

<p>1. Alle varer, der tilbydes i butikken, er helt nye, fri for juridiske fejl og er blevet lovligt introduceret til det britiske marked. Sælger er lovpligtig forpligtet til at levere varen fejlfri. Sælgers ansvar for kvaliteten af den ydelse, der er fastsat ved lov, er reguleret af de generelt gældende bestemmelser om varernes manglende overensstemmelse med kontrakten, især kapitel 5a i lov af 30. maj 2014 om forbrugerrettigheder.</p>

<p>2. I tilfælde af manglende overholdelse af varerne med kontrakten, kan kunden indgive en klage til sælgeren i enhver form, især skriftligt til følgende adresse: FORAM spółka z ograniczoną odpowiedzialnością sp. k., ul. Jeziorna 3, 64-200, Karpicko eller via e-mail til følgende adresse: <a href="mailto: b2b@artforma.eu">b2b@artforma.eu</a>. I klagen anbefales det at:</p>

<ul>
    <li>oplysning om reklamationens genstand, især type og dato for defekten</li>
    <li>definition af anmodningen vedrørende metoden til fjernelse af defekten (udskiftning af varerne med en ny, reparation af varerne og derefter prisreduktion eller tilbagetrækning fra kontrakten)</li>
    <li>angivelse af kontaktoplysninger på klageren</li>
</ul>

<p>hvilket vil lette og fremskynde butikkens behandling af klagen. Anbefalingerne i den foregående sætning er kun ikke-bindende retningslinjer og påvirker på ingen måde effektiviteten af klager, der indgives uden at give de anbefalede oplysninger.</p>

<p>3. Såfremt det er nødvendigt at behandle reklamationen, skal Kunden forsyne Sælger med de varer, der er genstand for reklamationen, og som er genstand for reparation eller ombytning. Sælger afhenter varerne hos kunden for egen regning. Hvis varerne blev installeret, før varernes manglende overensstemmelse med kontrakten blev afsløret, skiller Sælger varerne ad og samler dem igen efter reparation eller udskiftning, eller lader disse aktiviteter udføre for egen regning.</p>

<p>4. For at fremskynde behandlingen af klagen kan Kunden muliggøre undersøgelsen af klagen på afstand, især ved at levere billeder eller muliggøre inspektion i videokonferencetilstand. For at undgå besvær kan Kunden benytte muligheden for at undersøge reklamationen på afstand også i det tilfælde, hvor levering af produktet er mulig, en sådan beslutning begrænser ikke nogen af Kundens rettigheder.</p>

<p>5. Sælger er forpligtet til at besvare forbrugerens klage inden for 14 dage efter modtagelsen. I tilfælde af en anmodning om at udskifte varerne, fjerne deres mangler eller afgive en erklæring om fortrydelse af kontrakten baseret på bestemmelserne om varens manglende overensstemmelse med kontrakten (kapitel 5a i lov af 30. maj 2014 er køber forpligtet til at levere varen til forbrugeren). med forbehold for reparation eller ombytning.</p>

<p>6. Hvis Sælger anser reklamationen for uberettiget, vil varerne blive leveret til Kunden sammen med de relevante oplysninger.</p>

<p>7. I tilfælde af uberettiget undladelse af at afhente varerne, kan Kunden selv afhente varerne på Sælgers lager på et passende tidspunkt (på en hverdag fra kl. 9 til 15) efter at have informeret Sælger om den valgte dato. Eventuel genlevering af varerne fra Sælger eller levering til en anden angivet adresse er mulig ved særskilt aftale mellem Sælger.</p>

<p>8. Sælger er ansvarlig for varens manglende overensstemmelse med den kontrakt, der eksisterede på leveringstidspunktet og afsløret inden for to år fra dette tidspunkt. Varernes manglende overensstemmelse med kontrakten, som viste sig inden for to år fra leveringstidspunktet for varen, formodes at have eksisteret på leveringstidspunktet, medmindre andet bevises, eller formodningen ikke kan forenes med varens art eller arten af varens manglende overensstemmelse med kontrakten. .</p>

<p>9. For nogle varer yder sælger en yderligere garanti på de vilkår, der er angivet i garantidokumentet. Sælgerens garanti udelukker, begrænser eller suspenderer ikke rettighederne for kunden, der er en forbruger i henhold til bestemmelserne om varernes manglende overensstemmelse med kontrakten.</p>

<p><strong>Gælder for en ikke-forbrugerkunde:</strong></p>

<p>1. I tilfælde af kunder, der ikke er forbrugere i henhold til punkt I, stk. 2, i dette regulativ, i henhold til art. 558 § 1 i Civil Code, udelukker parterne Sælgers ansvar under garantien for mangler ved den solgte vare.</p>

<p>2. Det eneste ansvar i henhold til garantien, der henvises til i det foregående punkt, udelukker ikke Sælger fra at give en garanti for udvalgte Varer eller i løbet af individuelle forhandlinger med en Kunde, der ikke er en Forbruger.</p>

