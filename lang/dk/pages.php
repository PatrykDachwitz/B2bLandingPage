    <li>hvor emnet for tjenesten er varer, der forringes hurtigt eller har en kort holdbarhed.</li>
    <li>hvor genstand for ydelsen er varer leveret i en forseglet pakke, som ikke kan returneres efter åbning af pakken på grund af sundhedsbeskyttelse eller hygiejnemæssige årsager, hvis pakken blev åbnet efter levering.</li>
    <li>hvor genstand for tjenesteydelsen er varer, som på grund af deres natur er uadskilleligt forbundet med andre varer efter levering.</li>
    <li>hvor genstanden for tjenesten er alkoholholdige drikkevarer, hvis pris blev aftalt ved indgåelsen af salgskontrakten, og leveringen af disse kan først finde sted efter 30 dage, og hvis værdi afhænger af markedsudsving, som iværksætteren ikke har kontrol over.</li>
    <li>hvor forbrugeren udtrykkeligt krævede, at iværksætteren kom til ham for akut reparation eller vedligeholdelse; hvis iværksætteren leverer andre ydelser end dem, forbrugeren anmoder om, eller leverer andre genstande end reservedele, der er nødvendige for at udføre reparationen eller vedligeholdelsen, har forbrugeren ret til at fortryde kontrakten i forbindelse med yderligere ydelser eller genstande.</li>
    <li>hvor emnet for tjenesten er lyd- eller billedoptagelser eller computerprogrammer leveret i en forseglet pakke, hvis pakken blev åbnet efter levering.</li>
    <li>til levering af aviser, tidsskrifter eller magasiner, med undtagelse af en abonnementsaftale.</li>
    <li>afsluttet ved offentlig auktion.</li>
    <li>til levering af indkvarteringsydelser til andet end til beboelsesformål, transport af varer, biludlejning, gastronomi, tjenester relateret til fritid, underholdning, sport eller kulturelle begivenheder, hvis kontrakten specificerer dagen eller perioden for levering af tjenesteydelser.</li>
    <li>til levering af digitalt indhold, der ikke er optaget på et håndgribeligt medie, hvis udførelsen af tjenesten begyndte med forbrugerens udtrykkelige samtykke inden fristen for fortrydelse af kontrakten og efter at have informeret ham af iværksætteren om tabet af retten til at fortryde kontrakten.</li>
