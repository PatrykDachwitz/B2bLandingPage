<h2>XII. Slotbepalingen</h2>

<p>1. Eventuele geschillen die ontstaan in verband met de uitvoering van verkoopovereenkomsten tussen de Verkoper en de Klant, worden in de eerste plaats beslecht door middel van onderhandelingen, met de bedoeling het geschil in der minne te schikken.</p>

<p>2. Eventuele geschillen tussen de Verkoper en de Klant die geen Consument is, worden beslecht door de rechtbank die bevoegd is voor de vestigingsplaats van de Verkoper.</p>

<p>3. In zaken die niet onder deze Regeling vallen, zijn de bepalingen van het toepasselijke recht van toepassing, met name de bepalingen van de Wet van 23 april 1964. Burgerlijk Wetboek (geconsolideerde tekst, Staatsblad van 2014, item 121, zoals gewijzigd) en de Wet van 30 mei 1964. 2014 betreffende consumentenrechten (Staatsblad van 2014, item 827).</p>

<p>4. Op contracten die met de Verkoper worden gesloten, is het Poolse recht van toepassing. In het geval van consumentencontracten worden de dwingende bepalingen van het land van de woonplaats van de consument in acht genomen en de consument wordt door deze bepalingen beschermd, ongeacht de keuze van het Poolse recht als toepasselijk recht.</p>

