<h2>IX. Klachten over goederen</h2>

<p><strong>Geldt voor de Klant die een Consument is:</strong></p>

<p>1. Alle goederen die in de winkel worden aangeboden, zijn gloednieuw, vrij van wettelijke gebreken en zijn legaal op de Britse markt geïntroduceerd. De verkoper is wettelijk verplicht om goederen te leveren die vrij zijn van gebreken. De aansprakelijkheid van de verkoper voor de kwaliteit van de wettelijk voorziene service wordt geregeld door de algemeen geldende bepalingen over het gebrek aan overeenstemming van de goederen met het contract, met name hoofdstuk 5a van de wet van 30 mei 2014 over consumentenrechten.</p>

<p>2. In geval van niet-conformiteit van de goederen met het contract, kan de Klant een klacht indienen bij de Verkoper in welke vorm dan ook, met name schriftelijk op het volgende adres: FORAM spółka z ograniczoną odpowiedzialnością sp. k., ul. Jeziorna 3, 64-200, Karpicko of via e-mail op het volgende adres: <a href="mailto: b2b@artforma.eu">b2b@artforma.eu</a>. In de klacht wordt aanbevolen om:</p>

<ul>
    <li>het verstrekken van informatie over het onderwerp van de klacht, met name het type en de datum van het gebrek</li>
    <li>definitie van het verzoek met betrekking tot de methode om het gebrek te verhelpen (vervanging van de goederen door een nieuw, reparatie van de goederen en vervolgens prijsverlaging of ontbinding van het contract)</li>
    <li>het verstrekken van contactgegevens van de klager</li>
</ul>

<p>die de behandeling van de klacht door de winkel zal vergemakkelijken en versnellen. De aanbevelingen die in de vorige zin zijn gespecificeerd, zijn slechts niet-bindende richtlijnen en hebben op geen enkele manier invloed op de effectiviteit van klachten die zijn ingediend zonder de aanbevolen informatie te verstrekken.</p>

<p>3. Indien het nodig is om de klacht in overweging te nemen, zal de Klant de Verkoper voorzien van de goederen die het onderwerp zijn van de klacht, die onderhevig zijn aan reparatie of vervanging. De verkoper zal de goederen op eigen kosten bij de klant ophalen. Indien de goederen werden geïnstalleerd voordat de non-conformiteit van de goederen met het contract werd onthuld, demonteert de Verkoper de goederen en monteert ze opnieuw na reparatie of vervanging, of laat deze activiteiten op eigen kosten uitvoeren.</p>

<p>4. Om het onderzoek van de klacht te versnellen, kan de Klant het onderzoek van de klacht op afstand mogelijk maken, met name door foto\'s te verstrekken of inspectie in de videoconferentiemodus mogelijk te maken. Om ongemak te voorkomen, kan de Klant de mogelijkheid gebruiken om de klacht op afstand te onderzoeken, ook in het geval dat levering van het product mogelijk is, een dergelijke beslissing beperkt geen van de rechten van de Klant.</p>

<p>5. De verkoper is verplicht om binnen 14 dagen na ontvangst te reageren op de klacht van de consument. In het geval van een verzoek om vervanging van de goederen, het verwijderen van de gebreken of het indienen van een verklaring van herroeping van het contract op basis van de bepalingen over het gebrek aan overeenstemming van de goederen met het contract (Hoofdstuk 5a van de Wet van 30 mei 2014 betreffende consumentenrechten), is de Koper verplicht om de Verkoper de goederen te leveren die onderhevig zijn aan reparatie of omruiling.</p>

<p>6. Indien de Verkoper de klacht onterecht acht, dan worden de goederen samen met de relevante informatie aan de Klant geleverd.</p>

<p>7. In het geval van ongerechtvaardigd verzuim om de goederen op te halen, kan de Klant de goederen zelf ophalen bij het magazijn van de Verkoper, op een geschikt tijdstip (op een werkdag van 9.00 tot 15.00 uur) na de Verkoper te hebben geïnformeerd over de geselecteerde datum. Eventuele herlevering van de goederen door de Verkoper of levering op een ander aangegeven adres is mogelijk door een afzonderlijke overeenkomst tussen de Verkoper en de Klant.</p>

<p>8. De verkoper is aansprakelijk voor het gebrek aan overeenstemming van de goederen met het contract dat bestond op het moment van levering en dat zich binnen twee jaar vanaf dat moment openbaarde. Het gebrek aan overeenstemming van de goederen met het contract, dat zich manifesteerde binnen twee jaar vanaf het moment van levering van de goederen, wordt verondersteld te hebben bestaan op het moment van levering, tenzij het tegendeel wordt bewezen of het vermoeden niet kan worden verzoend met de aard van de goederen of de aard van het gebrek aan overeenstemming van de goederen met het contract. .</p>

<p>9. In het geval van sommige goederen verleent de verkoper een aanvullende garantie op de voorwaarden die in het garantiebewijs zijn opgenomen. De garantie die door de verkoper wordt verstrekt, sluit de rechten van de klant die een consument is, niet uit, beperkt of schort deze niet op, op grond van de bepalingen inzake niet-conformiteit van de goederen met het contract.</p>

<p><strong>Geldt voor een niet-consumentenklant:</strong></p>

<p>1. Voor Klanten die geen Consumenten zijn in de zin van punt I subsectie 2 van dit Reglement, sluiten partijen overeenkomstig art. 558 § 1 van het Burgerlijk Wetboek de aansprakelijkheid van de Verkoper uit hoofde van de garantie uit voor gebreken in het verkochte artikel.</p>

<p>2. De enige aansprakelijkheid onder de garantie waarnaar in het vorige punt wordt verwezen, sluit niet uit dat de Verkoper een garantie verleent voor geselecteerde Goederen of in het kader van individuele onderhandelingen met een Klant die geen Consument is.</p>

