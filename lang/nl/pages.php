        Met het bovenstaande in gedachten wijst de Verkoper erop dat het herroepingsrecht bij een overeenkomst op afstand zoals hieronder beschreven enkel geldt voor <strong>producten uit de Valentina-collectie (meubelsets)</strong>. </i></p>

<p>1. De klant die consument is, heeft het recht om binnen veertien dagen zonder opgave van redenen af te zien van de overeenkomst, overeenkomstig de bepalingen van de wet van 30 mei 2014 betreffende consumentenrechten. De termijn van veertien dagen begint te lopen vanaf de inbezitneming van de goederen door de Klant of een door hem aangewezen derde, die niet de vervoerder is, en in het geval van meerdere goederen die afzonderlijk, in partijen of in delen worden geleverd, vanaf de inbezitneming van de laatste goederen, hun partij of deel. Om de termijn te halen, volstaat het om vóór het verstrijken ervan een verklaring te sturen. Een verklaring van herroeping van de overeenkomst kan op elke manier worden ingediend, bijvoorbeeld via:</p>

<ul>
    <li>traditionele post: FORAM spółka z ograniczoną odpowiedzialnością sp. k., ul. Jeziorna 3, 64-200 Karpicko.</li>
    <li>e-mail: <a href="mailto: b2b@artforma.eu">b2b@artforma.eu</a></li>
    <li>telefonisch: <a href="tel: +442038085213">+44 203 808 5213</a>.</li>
</ul>

<p>2. De Klant kan ook een verklaring van herroeping van het contract indienen met behulp van het formulier dat is opgenomen in Bijlage 2 van de Wet van 30 mei 2014 betreffende consumentenrechten, waarvan het model ook hieronder in de Regeling is opgenomen. Dit model wordt bovendien samen met de bevestiging van de sluiting van de overeenkomst op afstand op een duurzame drager geleverd.</p>

<p>3. Indien de Klant de overeenkomst herroept, zal de Verkoper alle door de Klant gedane betalingen, inclusief de kosten voor levering van het artikel, onverwijld - maar uiterlijk binnen veertien dagen vanaf de datum waarop de verklaring van de Klant over de herroeping van de overeenkomst is ontvangen - aan de Klant terugbetalen.</p>

<p>4. Indien de Klant heeft gekozen voor een andere wijze van levering dan de door Verkoper aangeboden goedkoopste gebruikelijke wijze van levering, is Verkoper niet verplicht de door Klant gemaakte extra kosten, die de kosten van de door Verkoper aangeboden goedkoopste gebruikelijke wijze van levering te boven gaan, aan Klant te vergoeden.</p>

<p>5. De verkoper zal de betaling terugbetalen via dezelfde betaalmethode die door de klant is gebruikt.</p>

<p>6. De verkoper mag de terugbetaling van de van de klant ontvangen betalingen opschorten totdat hij de goederen terug heeft ontvangen of totdat de klant een bewijs van terugzending heeft geleverd, afhankelijk van welke gebeurtenis zich het eerst voordoet.</p>

<p>7. In geval van herroeping van het contract is de Klant verplicht om onmiddellijk, binnen een termijn van maximaal veertien dagen, de gekochte goederen volledig terug te sturen (samen met onder andere de Gebruiksaanwijzing en andere documenten), in een staat die niet verder gaat dan de manier van gebruik die nodig is om de aard, kenmerken en de werking van de zaken vast te stellen. De klant draagt de directe kosten van het terugsturen van de goederen. Om de deadline te halen, is het voldoende om het artikel terug te sturen voordat deze verloopt. Het wordt aanbevolen om de gekochte goederen binnen deze termijn terug te sturen naar het volgende adres: FORAM spółka z ograniczoną odpowiedzialnością sp. k., ul. Jeziorna 3, 64-200 Karpicko.</p>

<p>8. De klant is aansprakelijk voor een waardevermindering van de goederen die het gevolg is van een manier van gebruik die verder gaat dan nodig is om de aard, de kenmerken en de werking van de goederen vast te stellen, tenzij de verkoper de consument niet heeft geïnformeerd over het contractrecht overeenkomstig de vereisten van art. 12 sec. 1 punt 9 van de wet van 30 mei 2014 betreffende consumentenrechten. Vanwege het onderwerp van het transport raadt de verkoper aan, maar eist deze geen, een verzendverzekering bij de terugzending van de goederen.</p>

<p>9. De consument heeft geen herroepingsrecht bij een overeenkomst op afstand bij overeenkomsten:</p>

<ul>
    <li>bij levering van diensten, als de ondernemer de dienst volledig heeft verricht met uitdrukkelijke instemming van de consument, die vóór de aanvang van de dienst op de hoogte is gesteld van het feit dat hij na de uitvoering van de dienst door de ondernemer het herroepingsrecht verliest.</li>
    <li>waarbij de prijs of vergoeding afhankelijk is van schommelingen op de financiële markt waarop de ondernemer geen invloed heeft en die zich kunnen voordoen vóór het verstrijken van de termijn voor herroeping van de overeenkomst betreffende diensten met betrekking tot eigenschappen die door de consument in de door hem geplaatste bestelling zijn gespecificeerd of die nauw verband houden met zijn persoon.</li>
    <li><strong>waarbij het voorwerp van de dienst een niet-geprefabriceerd product is, dat volgens specificaties van de consument is vervaardigd of dat dient om aan zijn individuele behoeften te voldoen.</strong></li>
    <li>waarbij het onderwerp van de dienst goederen zijn die snel bederven of een korte houdbaarheid hebben.</li>
    <li>waarbij het voorwerp van de dienst goederen zijn die in een verzegelde verpakking worden geleverd, die om redenen van gezondheidsbescherming of hygiëne na opening van de verpakking niet kunnen worden geretourneerd, indien de verpakking na levering is geopend.</li>
    <li>waarbij het voorwerp van de dienst bestaat uit goederen die na levering door hun aard onlosmakelijk met andere zaken zijn verbonden.</li>
    <li>waarbij het voorwerp van de dienst alcoholische dranken zijn, waarvan de prijs is overeengekomen bij het sluiten van de koopovereenkomst en waarvan de levering pas na 30 dagen kan plaatsvinden en waarvan de waarde afhankelijk is van schommelingen van de markt waarop de ondernemer geen invloed heeft.</li>
    <li>waarbij de consument uitdrukkelijk heeft verzocht dat de ondernemer voor spoedeisende reparatie of onderhoud bij hem langskomt; indien de ondernemer aanvullende diensten verleent die niet door de consument zijn gevraagd, of zaken levert die niet noodzakelijk zijn voor de reparatie of het onderhoud, heeft de consument het recht de overeenkomst met betrekking tot die aanvullende diensten of zaken te ontbinden.</li>
    <li>waarbij het onderwerp van de dienst bestaat uit geluids- of beeldopnamen of computerprogramma\'s die in een verzegelde verpakking worden geleverd, indien de verpakking na levering is geopend.</li>
    <li>voor de levering van kranten, tijdschriften of magazines, met uitzondering van een abonnementsovereenkomst.</li>
    <li>afgesloten via openbare veiling.</li>
    <li>voor het verlenen van accommodatiediensten anders dan voor woondoeleinden, goederenvervoer, autoverhuur, gastronomie, diensten met betrekking tot vrijetijdsbesteding, amusement, sport- of culturele evenementen, indien in het contract de dag of periode van de dienstverlening is gespecificeerd.</li>
    <li>voor de levering van digitale inhoud die niet op een materiële drager is vastgelegd, als de uitvoering van de dienst is begonnen met uitdrukkelijke instemming van de consument vóór het verstrijken van de termijn voor herroeping van de overeenkomst en nadat de ondernemer hem heeft geïnformeerd over het verlies van het herroepingsrecht.</li>
</ul>

