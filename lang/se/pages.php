<p>3. I händelse av att Kunden frånträder avtalet ska Säljaren omedelbart - men senast inom fjorton dagar från dagen för mottagandet av Kundens utlåtande om frånträdande av avtalet - återbetala till Kunden alla betalningar som denne gjort, inklusive kostnaderna för leverans av varan.</p>

<p>4. Om Kunden har valt ett annat sätt att leverera varan än det billigaste vanliga leveranssättet som Säljaren erbjuder, är Säljaren inte skyldig att ersätta Kunden för merkostnader som han ådragit sig, utöver kostnaderna för det billigaste vanliga leveranssättet som Säljaren erbjuder.</p>

<p>5. Säljaren kommer att återbetala betalningen med samma betalningsmetod som används av kunden.</p>

<p>6. Säljaren kan hålla inne återbetalningen av betalningar som erhållits från kunden till dess att varorna har mottagits tillbaka eller kunden tillhandahåller bevis på dess retur, beroende på vilken händelse som inträffar först.</p>

<p>7. I händelse av frånträde från avtalet är Kunden skyldig att omedelbart, inom en period som inte överstiger fjorton dagar, returnera de köpta varorna i sin helhet (tillsammans med bland annat användarmanualen och andra dokument), i ett skick som inte överstiger det sätt att använda den som är nödvändig för att fastställa sakernas art, egenskaper och funktion. Kunden står för den direkta kostnaden för att returnera varan. För att hålla tidsfristen räcker det att skicka tillbaka varan innan dess utgång. Det rekommenderas att skicka tillbaka de köpta varorna inom denna period till följande adress: FORAM spółka z ograniczoną odpowiedzialnością sp. k., ul. Jeziorna 3, 64-200 Karpicko.</p>

<p>8. Kunden ansvarar för en minskning av varans värde till följd av att den används på ett sätt som går utöver vad som är nödvändigt för att fastställa varans art, egenskaper och funktion, om inte Säljaren inte har informerat konsumenten om rätten att ingå avtal i enlighet med kraven i art. 12 sek. 1 punkt 9 i lagen den 30 maj 2014 om konsumenträttigheter. På grund av föremålet för transporten rekommenderar Säljaren, men kräver inte, fraktförsäkring med retur av varorna.</p>

<p>9. Konsumenten har inte rätt att frånträda ett distansavtal i samband med avtal:</p>

<ul>
    <li>för tillhandahållande av tjänster, om företagaren till fullo har utfört tjänsten med uttryckligt samtycke från konsumenten, som informerades innan tjänsten påbörjades att han kommer att förlora rätten att ångerrätt efter att företagaren har utfört tjänsten.</li>
    <li>där priset eller ersättningen beror på fluktuationer på finansmarknaden som företagaren inte har kontroll över och som kan inträffa före utgången av tidsfristen för att frånträda avtalet för tjänster med egenskaper som specificeras av konsumenten i den beställning som lagts av honom eller närstående till hans person .</li>
    <li><strong>där föremålet för tjänsten är en icke-prefabricerad produkt, tillverkad enligt konsumentens specifikationer eller tjänar till att tillfredsställa dennes individuella behov.</strong></li>
    <li>där föremålet för tjänsten är varor som försämras snabbt eller har kort hållbarhet.</li>
    <li>där föremålet för tjänsten är varor levererade i en förseglad förpackning, som inte kan returneras efter att förpackningen öppnats på grund av hälsoskydds- eller hygienskäl, om förpackningen öppnades efter leverans.</li>
    <li>där föremålet för tjänsten är varor som på grund av sin natur är oskiljaktigt förbundna med andra föremål efter leverans.</li>
    <li>där föremålet för tjänsten är alkoholhaltiga drycker, vars pris överenskoms vid ingåendet av försäljningsavtalet, och vars leverans får ske först efter 30 dagar och vars värde beror på marknadsfluktuationer som företagaren inte har någon kontroll över.</li>
    <li>där konsumenten uttryckligen krävde att företagaren skulle komma till honom för brådskande reparation eller underhåll; om företagaren tillhandahåller andra tilläggstjänster än de som efterfrågas av konsumenten, eller tillhandahåller andra föremål än reservdelar som är nödvändiga för att utföra reparationen eller underhållet, har konsumenten rätt att frånträda avtalet i samband med tilläggstjänster eller föremål.</li>
    <li>där ämnet för tjänsten är ljud- eller bildinspelningar eller datorprogram levererade i en förseglad förpackning, om förpackningen öppnades efter leverans.</li>
    <li>för leverans av tidningar, tidskrifter eller tidskrifter, med undantag för ett prenumerationsavtal.</li>
    <li>avslutad genom offentlig auktion.</li>
    <li>för tillhandahållande av andra boendetjänster än för bostadsändamål, transport av varor, biluthyrning, gastronomi, tjänster relaterade till fritid, underhållning, sport eller kulturevenemang, om avtalet anger dagen eller perioden för tillhandahållandet av tjänster.</li>
    <li>för leverans av digitalt innehåll som inte är inspelat på ett materiellt medium, om utförandet av tjänsten påbörjades med konsumentens uttryckliga samtycke före tidsfristen för att frånträda avtalet och efter att företagaren informerat honom om förlusten av rätten att frånträda avtalet.</li>
</ul>

