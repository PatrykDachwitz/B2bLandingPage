<h2>IX. Klagomål angående varor</h2>

<p><strong>Gäller Kunden som är Konsument:</strong></p>

<p>1. Alla varor som erbjuds i butiken är helt nya, fria från juridiska defekter och har lagligen introducerats på den brittiska marknaden. Säljaren har en lagstadgad skyldighet att leverera varor utan defekter. Säljarens ansvar för kvaliteten på tjänsten enligt lag regleras av de allmänt tillämpliga bestämmelserna om varornas bristande överensstämmelse med avtalet, särskilt kapitel 5a i lagen av den 30 maj 2014 om konsumenträttigheter.</p>

<p>2. I händelse av bristande överensstämmelse med varorna med avtalet kan kunden lämna in ett klagomål till säljaren i vilken form som helst, särskilt skriftligt till följande adress: FORAM spółka z ograniczoną odpowiedzialnością sp. k., ul. Jeziorna 3, 64-200, Karpicko eller via e-post till följande adress: <a href="mailto: b2b@artforma.eu">b2b@artforma.eu</a>. I klagomålet rekommenderas att:</p>

<ul>
    <li>tillhandahålla information om föremålet för reklamationen, särskilt typ och datum för defekten</li>
    <li>definition av begäran om metoden för att avlägsna defekten (ersättning av varorna med en ny, reparation av varorna och sedan prisavdrag eller frånträde från avtalet)</li>
    <li>ange kontaktuppgifter till klaganden</li>
</ul>

<p>vilket kommer att underlätta och påskynda behandlingen av klagomålet i butiken. Rekommendationerna i föregående mening är endast icke-bindande riktlinjer och påverkar inte på något sätt effektiviteten av klagomål som lämnas in utan att tillhandahålla den rekommenderade informationen.</p>

<p>3. Om det är nödvändigt att överväga reklamationen, ska Kunden förse Säljaren med de varor som är föremål för reklamationen, vilka är föremål för reparation eller utbyte. Säljaren hämtar varorna från kunden på egen bekostnad. Om varorna installerades innan varornas bristande överensstämmelse med avtalet avslöjades, tar säljaren isär varorna och sätter ihop dem igen efter reparation eller utbyte, eller låter dessa aktiviteter utföras på egen bekostnad.</p>

<p>4. För att påskynda prövningen av klagomålet kan Kunden möjliggöra undersökning av klagomålet på distans, särskilt genom att tillhandahålla bilder eller möjliggöra inspektion i videokonferensläget. För att undvika besvär kan Kunden använda möjligheten att undersöka reklamationen på distans även i de fall där leverans av produkten är möjlig, ett sådant beslut begränsar inte någon av Kundens rättigheter.</p>

<p>5. Säljaren är skyldig att svara på konsumentens reklamation inom 14 dagar efter mottagandet. I händelse av en begäran om att byta ut varan, ta bort deras defekter eller lämna in en förklaring om frånträde från avtalet baserat på bestämmelserna om varornas bristande överensstämmelse med avtalet (kapitel 5a i lagen av 30 maj 2014 är köparen skyldig att tillhandahålla köparen rätt till konsumenten), föremål för reparation eller byte.</p>

<p>6. Om Säljaren anser att reklamationen är obefogad, kommer varorna att levereras till Kunden tillsammans med relevant information.</p>

<p>7. I händelse av omotiverad underlåtenhet att hämta ut varorna kan Kunden hämta varorna från Säljarens lager på egen hand, vid lämplig tidpunkt (en arbetsdag kl. 9-15) efter att ha informerat Säljaren om valt datum. Eventuell återleverans av varorna av Säljaren eller leverans till annan angiven adress är möjlig genom ett separat avtal mellan Säljaren.</p>

<p>8. Säljaren är ansvarig för varans bristande överensstämmelse med det avtal som existerade vid leveranstillfället och avslöjas inom två år från den tidpunkten. Varans bristande överensstämmelse med avtalet, som visade sig inom två år från tidpunkten för leveransen av varan, ska antas ha funnits vid leveranstillfället, om inte annat bevisas eller presumtionen inte kan förenas med varans beskaffenhet eller arten av varans bristande överensstämmelse med avtalet. .</p>

<p>9. För vissa varor ger Säljaren ytterligare garanti på de villkor som anges i garantidokumentet. Garantin som tillhandahålls av säljaren utesluter, begränsar eller upphäver inte rättigheterna för kunden som är en konsument enligt bestämmelserna om varornas bristande överensstämmelse med avtalet.</p>

<p><strong>Gäller en icke-konsumentkund:</strong></p>

