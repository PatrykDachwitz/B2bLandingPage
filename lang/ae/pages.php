<p>4. ينطبق القانون البولندي على العقود المبرمة مع البائع في حالة عقود المستهلكين، سيتم احترام الأحكام الإلزامية لبلد مكان إقامة المستهلك، ويحمي المستهلك هذه الأحكام بغض النظر عن اختيار القانون البولندي كقانون قابل للتطبيق.</p>

