<p>9. بالنسبة لبعض السلع، يمنح Seller ضمانًا إضافيًا وفقًا للشروط المنصوص عليها في وثيقة الضمان لا يستبعد الضمان المقدم من Seller أو يحد أو يعلق حقوق العميل الذي يُعتبر مستهلكًا بموجب الأحكام المتعلقة بعدم توافق السلع مع العقد</p>

<p><strong>ينطبق على العميل غير المستهلك</strong></p>

<p>1. في حالة العملاء الذين ليسوا مستهلكين بالمعنى الوارد في النقطة I الفقرة 2 من هذه اللوائح، وفقًا للمادة 558 § 1 من القانون المدني، يستبعد الطرفان مسؤولية Seller بموجب الضمان عن عيوب العنصر المباع</p>

<p>2. المسؤولية الوحيدة بموجب الضمان المشار إليها في النقطة السابقة لا تستبعد منح Seller ضمانًا لبعض السلع أو التفاوض الفردي مع عميل لا يُعتبر مستهلكًا</p>



<h2>X. الشكاوى المتعلقة بتقديم الخدمات الإلكترونية</h2>

<p>1. يتخذ البائع خطوات لضمان التشغيل السليم للمتجر الإلكتروني، بما يتناسب مع المعرفة التقنية الحالية، ويتعهد بإزالة أي مخالفات يتم الإبلاغ عنها من قبل العملاء في غضون وقت معقول.</p>

<p>2. يمكن للعميل إبلاغ البائع بأي مخالفات أو انقطاعات في تشغيل المتجر الإلكتروني. يجب الإبلاغ عن المخالفات المتعلقة بتشغيل المتجر الإلكتروني إلكترونيًا على عنوان البريد الإلكتروني التالي: b2b@artforma.eu</p>

<p>3. في الشكوى المتعلقة بالمخالفات المتعلقة بتشغيل المتجر الإلكتروني، يجب تحديد نوع المخالفة وتاريخها.</p>

<p>4. سيقوم البائع بالرد على الشكوى فورًا، وذلك في موعد لا يتجاوز 14 يومًا من تاريخ استلامها.</p>



<h2>XI. الحادي عشر. الطرق غير القضائية للتعامل مع الشكاوى ومتابعة المطالبات</h2>

<p>1. يُعلم البائع بإمكانية استخدام الطرق غير القضائية للتعامل مع الشكاوى ومتابعة المطالبات. استخدام هذه الطرق طوعي ولا يمكن أن يتم إلا عندما يتفق الطرفان المتنازعان على ذلك.</p>

<ul>
    <li>يمكن للمستهلك التقدم بطلب لبدء إجراءات لحل النزاعات المتعلقة بالمستهلكين بشأن عقد البيع المبرم خارج المحكمة إلى مفتشية التجارة، وفقًا للمادة 36 من قانون 15 ديسمبر 2000 بشأن مفتشية التجارة (الجريدة الرسمية لعام 2001 رقم 4، المادة 25، كما تم تعديله).</li>
    <li>يمكن للمستهلك أيضًا التقدم بطلب لحل النزاع المتعلق بعقد البيع المبرم من قبل محكمة تحكيم دائمة تعمل في المفتشية الإقليمية المناسبة لمفتشية التجارة، وفقًا للمادة 37 من قانون 15 ديسمبر 2000 بشأن مفتشية التجارة (الجريدة الرسمية لعام 2001 رقم 4، المادة 25، كما تم تعديله).</li>
    <li>توفر المفوضية الأوروبية أيضًا منصة لحل النزاعات عبر الإنترنت بين المستهلكين ورجال الأعمال (منصة ODR). وهي متوفرة على الرابط: http://ec.europa.eu/consumers/odr/</li>
</ul>

<p>2. تتوفر معلومات مفصلة حول حل النزاعات المتعلقة بالمستهلكين، بما في ذلك إمكانية استخدام المستهلك للطرق غير القضائية للتعامل مع الشكاوى، ومتابعة المطالبات، وقواعد الوصول إلى هذه الإجراءات في المقرات وعلى مواقع الإنترنت للمفتشيات الإقليمية لمفتشية التجارة وعلى الرابط: https://uokik.gov.pl/spory_konsumenckie.php</p>


<h2>XII. الأحكام الختامية</h2>

<p>1. أي نزاعات تنشأ في إطار تنفيذ عقود البيع بين البائع والعميل سيتم تسويتها في المقام الأول من خلال المفاوضات، بهدف تسوية النزاع بشكل ودي.</p>

<p>2. تسوية أي نزاعات تنشأ بين البائع والعميل الذي لا يُعتبر مستهلكًا تخضع للمحكمة المختصة في مكان إقامة البائع</p>

