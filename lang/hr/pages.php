<h2>IX. Reklamacije u vezi s Robom</h2>

<p><strong>Primjenjuje se na kupca koji je potrošač:</strong></p>

<p>1. Sva roba ponuđena u Trgovini potpuno je nova, bez pravnih nedostataka i legalno je uvedena na tržište Ujedinjenog Kraljevstva. Prodavatelj ima zakonsku obvezu isporučiti robu bez nedostataka. Odgovornost prodavatelja za kvalitetu usluge propisane zakonom regulirana je opće primjenjivim odredbama o nesukladnosti robe s ugovorom, posebice poglavljem 5a Zakona o pravima potrošača od 30. svibnja 2014.</p>

<p>2. U slučaju neusklađenosti robe s ugovorom, Kupac može podnijeti pritužbu Prodavatelju u bilo kojem obliku, a posebno u pisanom obliku na sljedeću adresu: FORAM spółka z ograniczoną odpowiedzialnością sp. k., ul. Jeziorna 3, 64-200, Karpičko ili putem e-maila na adresu: <a href="mailto: b2b@artforma.eu">b2b@artforma.eu</a>. U prigovoru se preporuča:</p>

<ul>
<li>pružanje informacija o predmetu pritužbe, posebno vrsti i datumu kvara</li>
<li>definiranje zahtjeva u pogledu načina otklanjanja nedostatka (zamjena robe novom, popravak robe, a zatim sniženje cijene ili odustajanje od ugovora)</li>
<li>navođenje podataka za kontakt podnositelja pritužbe</li>
</ul>

<p>što će olakšati i ubrzati razmatranje pritužbe od strane Trgovine. Preporuke navedene u prethodnoj rečenici samo su neobvezujuće smjernice i ni na koji način ne utječu na učinkovitost pritužbi podnesenih bez pružanja preporučenih informacija.</p>

<p>3. Ukoliko je potrebno razmotriti reklamaciju, Kupac je dužan Prodavatelju dostaviti robu koja je predmet reklamacije, a koja podliježe popravku ili zamjeni. Prodavatelj preuzima robu od kupca o svom trošku. Ako je roba ugrađena prije nego što je otkrivena nesukladnost robe s ugovorom, Prodavatelj nakon popravka ili zamjene robu rastavlja i ponovno sastavlja ili te radnje obavlja o svom trošku.</p>

<p>4. Kako bi se ubrzalo ispitivanje prigovora, Kupac može omogućiti ispitivanje prigovora na daljinu, posebice dostavom fotografija ili omogućavanjem uvida u videokonferencijskom načinu rada. Kako bi izbjegao neugodnosti, Kupac može iskoristiti mogućnost daljinskog ispitivanja reklamacije iu slučaju kada je moguća isporuka proizvoda, takvom odlukom se ne ograničavaju prava Kupca.</p>

<p>5. Prodavatelj je dužan odgovoriti na prigovor potrošača u roku od 14 dana od dana primitka prigovora. U slučaju zahtjeva za zamjenu robe, otklanjanje nedostataka ili podnošenja izjave o odustajanju od ugovora temeljem odredbi o nesukladnosti robe s ugovorom (poglavlje 5.a Zakona o pravima potrošača od 30. svibnja 2014.), Kupac je dužan prodavatelju predati robu podložnu popravak ili zamjena.</p>

<p>6. Ukoliko Prodavatelj ocijeni da je reklamacija neopravdana, roba će biti isporučena Kupcu zajedno s relevantnim podacima.</p>

<p>7. U slučaju neopravdanog nepreuzimanja robe, Kupac može sam preuzeti robu iz skladišta Prodavatelja, u prigodno vrijeme (radnim danom od 9 do 15 sati) nakon što obavijesti Prodavatelja o odabranom datumu. Eventualna ponovna isporuka robe od strane Prodavatelja ili dostava na drugu naznačenu adresu moguća je posebnim dogovorom između Prodavatelja i Kupca.</p>

<p>8. Prodavatelj je odgovoran za nesukladnost robe s ugovorom koja postoji u trenutku isporuke i otkrivena je u roku od dvije godine od tog trenutka. Pretpostavlja se da je nesukladnost robe s ugovorom, koja je postala očita u roku od dvije godine od trenutka isporuke robe, postojala u trenutku isporuke, osim ako se dokaže drugačije ili se pretpostavka ne može uskladiti s prirodom robe ili prirodom nesukladnosti robe s ugovorom. .</p>

<p>9. U slučaju neke Robe, Prodavatelj daje dodatno jamstvo pod uvjetima navedenim u jamstvenom dokumentu. Jamstvo koje daje Prodavatelj ne isključuje, ograničava ili obustavlja prava Kupca koji je Potrošač prema odredbama o nesukladnosti robe s ugovorom.</p>

<p><strong>Primjenjuje se na kupce koji nisu potrošači:</strong></p>

<p>1. U slučaju kupaca koji nisu potrošači u smislu točke I. pododjeljka 2. ovih Pravila, sukladno čl. 558 § 1 Građanskog zakonika, strane isključuju odgovornost prodavatelja prema jamstvu za nedostatke prodanog predmeta.</p>

