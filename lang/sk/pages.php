<h2>XII. Záverečné ustanovenia</h2>

<p>1. Akékoľvek spory vzniknuté v súvislosti s plnením zmlúv o predaji medzi predávajúcim a zákazníkom sa budú najprv riešiť prostredníctvom rokovaní, s cieľom dosiahnuť zmierlivé riešenie sporu.</p>

<p>2. Riešenie akýchkoľvek sporov vzniknutých medzi predávajúcim a zákazníkom, ktorý nie je spotrebiteľom, podlieha súdu príslušnému pre sídlo predávajúceho.</p>

<p>3. Na záležitosti, ktoré nie sú pokryté týmito pravidlami, sa vzťahujú ustanovenia platného práva, najmä ustanovenia Zákona zo dňa 23. apríla 1964. Občiansky zákonník (zjednotený text, Zbierka zákonov 2014, položka 121, v znení neskorších predpisov) a Zákona zo dňa 30. mája 2014 o právach spotrebiteľov (Zbierka zákonov 2014, položka 827).</p>

<p>4. Na zmluvy uzatvorené s predávajúcim sa vzťahuje poľské právo. V prípade zmlúv so spotrebiteľom budú rešpektované povinné ustanovenia krajiny, v ktorej má spotrebiteľ bydlisko, a spotrebiteľ je chránený týmito ustanoveniami bez ohľadu na voľbu poľského práva ako platného práva.</p>

