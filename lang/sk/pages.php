<p>1. V prípade zákazníkov, ktorí nie sú spotrebiteľmi v zmysle bodu I, pododsek 2 týchto pravidiel, podľa článku 558 § 1 Občianskeho zákonníka, strany vylučujú zodpovednosť predávajúceho zárukou za vady predaného tovaru.</p>

<p>2. Jediná zodpovednosť podľa záruky uvedená v predchádzajúcom bode nebráni predávajúcemu poskytnúť záruku na vybraný tovar alebo v priebehu individuálnych rokovaní so zákazníkom, ktorý nie je spotrebiteľom.</p>

