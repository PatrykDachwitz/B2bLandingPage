<p>3. I tilfelle Kundens heving fra kontrakten, skal Selger umiddelbart - men senest innen fjorten dager fra datoen for mottak av Kundens erklæring om angrerett fra kontrakten - returnere Kunden alle betalinger han har foretatt, inkludert kostnadene ved levering av varen.</p>

<p>4. Dersom Kunden har valgt en annen måte å levere varene på enn den billigste vanlige leveringsmåten som Selger tilbyr, er Selger ikke forpliktet til å refundere Kunden for tilleggskostnader han pådrar seg, utover kostnadene ved den billigste vanlige leveringsmåten Selger tilbyr.</p>

<p>5. Selgeren vil refundere betalingen med samme betalingsmetode som kunden bruker.</p>

<p>6. Selgeren kan holde tilbake refusjon av betalinger mottatt fra kunden inntil mottak av varene tilbake eller kunden fremlegger bevis for retur, avhengig av hvilken hendelse som inntreffer først.</p>

<p>7. Ved heving av kontrakten er Kunden forpliktet til umiddelbart, innen en periode på ikke over fjorten dager, å returnere de kjøpte varene i sin helhet (sammen med blant annet brukerhåndboken og andre dokumenter), i en stand som ikke overstiger måten å bruke den på som er nødvendig for å fastslå tingenes art, egenskaper og funksjon. Kunden bærer de direkte kostnadene ved å returnere varene. For å overholde fristen er det nok å sende tilbake varen før den utløper. Det anbefales å sende tilbake de kjøpte varene innen denne perioden til følgende adresse: FORAM spółka z ograniczoną odpowiedzialnością sp. k., ul. Jeziorna 3, 64-200 Karpicko.</p>

<p>8. Kunden er ansvarlig for verdifall på varen som følge av å bruke den på en måte som går utover det som er nødvendig for å fastslå varens art, egenskaper og funksjon, med mindre Selger ikke har informert forbrukeren om retten til å inngå avtale i henhold til kravene i art. 12 sek. 1 pkt. 9 i lov av 30. mai 2014 om forbrukerrettigheter. På grunn av gjenstanden for transporten anbefaler, men krever ikke, fraktforsikring med retur av varene.</p>

<p>9. Forbrukeren har ikke rett til å si opp en fjernavtale i forhold til kontrakter:</p>

<ul>
<li>for levering av tjenester, hvis entreprenøren fullt ut har utført tjenesten med uttrykkelig samtykke fra forbrukeren, som ble informert før tjenesten startet om at etter at gründeren har utført tjenesten, vil han miste retten til å angre.</li>
<li>hvor prisen eller godtgjørelsen avhenger av svingninger i finansmarkedet som entreprenøren ikke har kontroll over, og som kan oppstå før utløpet av fristen for å trekke seg fra kontrakten for tjenester med egenskaper spesifisert av forbrukeren i bestillingen av ham eller nært knyttet til hans person .</li>
<li><strong>hvor emnet for tjenesten er et ikke-prefabrikkert produkt, produsert i henhold til forbrukerens spesifikasjoner eller tjener for å tilfredsstille hans individuelle behov.</strong></li>
<li>hvor emnet for tjenesten er varer som forringes raskt eller har kort holdbarhet.</li>
<li>hvor gjenstand for tjenesten er varer levert i en forseglet pakke, som ikke kan returneres etter åpning av pakken på grunn av helsevern eller hygienegrunner, dersom pakken ble åpnet etter levering.</li>
<li>hvor gjenstanden for tjenesten er varer som på grunn av sin natur er uadskillelig forbundet med andre varer etter levering.</li>
<li>hvor gjenstanden for tjenesten er alkoholholdige drikkevarer, hvis pris ble avtalt ved inngåelsen av salgskontrakten, og leveringen av disse kan skje først etter 30 dager og verdien avhenger av markedssvingninger som gründeren ikke har kontroll over.</li>
<li>hvor forbrukeren eksplisitt krevde at entreprenøren skulle komme til ham for umiddelbar reparasjon eller vedlikehold; hvis gründeren yter andre tilleggstjenester enn de som forbrukeren ber om, eller leverer andre ting enn reservedeler som er nødvendige for å utføre reparasjonen eller vedlikeholdet, har forbrukeren rett til å trekke seg fra kontrakten i forhold til tilleggstjenester eller -gjenstander.</li>
<li>hvor emnet for tjenesten er lyd- eller bildeopptak eller dataprogrammer levert i en forseglet pakke, hvis pakken ble åpnet etter levering.</li>
<li>for levering av aviser, tidsskrifter eller magasiner, med unntak av en abonnementsavtale.</li>
<li>avsluttet av offentlig auksjon.</li>
<li>for levering av overnattingstjenester annet enn til boligformål, transport av varer, bilutleie, gastronomi, tjenester knyttet til fritid, underholdning, sport eller kulturelle arrangementer, hvis kontrakten spesifiserer dagen eller perioden for tjenesteytelsen.</li>
<li>for levering av digitalt innhold som ikke er registrert på et håndgripelig medium, hvis ytelsen av tjenesten begynte med uttrykkelig samtykke fra forbrukeren før fristen for å trekke seg fra kontrakten og etter å ha informert ham av gründeren om tapet av retten til å trekke seg fra kontrakten.</li>
</ul>

