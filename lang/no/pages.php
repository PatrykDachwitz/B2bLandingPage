<h2>X. Klager angående levering av elektroniske tjenester</h2>

<p>1. Selgeren tar skritt for å sikre riktig drift av nettbutikken, i den grad det er et resultat av gjeldende tekniske kunnskap, og forplikter seg til å fjerne eventuelle uregelmessigheter rapportert av kunder innen rimelig tid.</p>

<p>2. Kunden kan varsle Selger om eventuelle uregelmessigheter eller avbrudd i nettbutikkens funksjon. Uregelmessigheter knyttet til funksjonen til nettbutikken skal rapporteres elektronisk til følgende e-postadresse: <a href="mailto: b2b@artforma.eu">b2b@artforma.eu</a></p>

<p>3. I en klage angående uregelmessigheter knyttet til funksjonen til nettbutikken, bør type og dato for uregelmessigheten angis.</p>

