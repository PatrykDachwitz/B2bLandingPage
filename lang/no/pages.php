<h2>XI. Utenrettslige metoder for å håndtere klager og forfølge krav</h2>

<p>1. Selger informerer om muligheten for å bruke utenrettslige metoder for å behandle klager og forfølge krav. Å bruke dem er frivillig og kan bare finne sted når begge parter i tvisten er enige om det.</p>

<ul>
<li>Forbrukeren kan søke om igangsetting av prosedyre for utenrettslig løsning av forbrukertvister angående den inngåtte salgsavtalen til Handelstilsynet, i henhold til art. 36 i lov av 15. desember 2000 om handelstilsynet (Lovtidsskrift 2001 nr. 4, pkt. 25, med senere endringer).</li>
<li>Forbrukeren kan også søke om å få en tvist angående den inngåtte salgsavtalen løst av en permanent voldgiftsdomstol som opererer ved det aktuelle voivodship-inspektoratet til handelstilsynet, i samsvar med art. 37 i lov av 15. desember 2000 om handelstilsynet (Lovtidsskrift 2001 nr. 4, pkt. 25, med senere endringer).</li>
<li>Europakommisjonen tilbyr også en plattform for nettbasert tvisteløsning mellom forbrukere og entreprenører (ODR-plattform). Den er tilgjengelig på: http://ec.europa.eu/consumers/odr/</li>
</ul>

<p>2. Detaljert informasjon om løsning av forbrukertvister, inkludert muligheten for forbrukeren til å bruke utenrettslige metoder for å håndtere klager, forfølge krav og reglene for tilgang til disse prosedyrene er tilgjengelig på hovedkvarteret og på nettsidene til voivodship-inspektoratene til handelsinspeksjonen og på: https://uokik.gov. pl/spory_konsumenckie.php</p>

