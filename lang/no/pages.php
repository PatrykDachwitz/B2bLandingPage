<h2>IX. Klager angående varer</h2>

<p><strong>Gjelder for kunden som er en forbruker:</strong></p>

<p>1. Alle varer som tilbys i butikken er helt nye, fri for juridiske mangler og har blitt lovlig introdusert til det britiske markedet. Selger er forpliktet til å levere varen feilfri. Selgers ansvar for kvaliteten på tjenesten i henhold til loven er regulert av de generelt gjeldende bestemmelsene om varens manglende samsvar med kontrakten, spesielt kapittel 5a i lov av 30. mai 2014 om forbrukerrettigheter.</p>

<p>2. I tilfelle av manglende overholdelse av varene med kontrakten, kan kunden sende en klage til selgeren i enhver form, spesielt skriftlig til følgende adresse: FORAM spółka z ograniczoną odpowiedzialnością sp. k., ul. Jeziorna 3, 64-200, Karpicko eller via e-post til følgende adresse: <a href="mailto: b2b@artforma.eu">b2b@artforma.eu</a>. I klagen anbefales det å:</p>

<ul>
<li>gi informasjon om gjenstanden for reklamasjonen, spesielt type og dato for defekten</li>
<li>definisjon av forespørselen angående metoden for å fjerne defekten (erstatning av varene med en ny, reparasjon av varene, og deretter prisreduksjon eller tilbaketrekking fra kontrakten)</li>
<li>oppgi kontaktinformasjonen til klageren</li>
</ul>

<p>noe som vil lette og fremskynde behandlingen av klagen i butikken. Anbefalingene spesifisert i forrige setning er bare ikke-bindende retningslinjer og påvirker på ingen måte effektiviteten til klager som sendes inn uten å gi den anbefalte informasjonen.</p>

<p>3. Dersom det er nødvendig å vurdere reklamasjonen, skal Kunden gi Selger varene som er gjenstand for reklamasjonen, som er gjenstand for reparasjon eller utskifting. Selger vil hente varene fra kunden for egen regning. Hvis varene ble installert før varens manglende samsvar med kontrakten ble avslørt, demonterer selgeren varene og monterer dem på nytt etter reparasjon eller utskifting, eller får disse aktivitetene utført for egen regning.</p>

<p>4. For å fremskynde undersøkelsen av klagen, kan Kunden muliggjøre undersøkelse av klagen på avstand, spesielt ved å gi bilder eller muliggjøre inspeksjon i videokonferansemodus. For å unngå ulemper, kan Kunden benytte muligheten til å undersøke klagen eksternt også i tilfelle levering av produktet er mulig, en slik beslutning begrenser ikke noen av Kundens rettigheter.</p>

<p>5. Selgeren er forpliktet til å svare på forbrukerens klage innen 14 dager etter at den er mottatt. Ved forespørsel om å erstatte varene, fjerne deres mangler eller fremlegge en erklæring om angrerett fra kontrakten basert på bestemmelsene om varens manglende samsvar med kontrakten (kapittel 5a i lov av 30. mai 2014 er kjøperen forpliktet til å gi kjøperen rett til forbrukeren), gjenstand for reparasjon eller bytte.</p>

<p>6. Hvis selgeren anser reklamasjonen uberettiget, vil varene bli levert til kunden sammen med relevant informasjon.</p>

<p>7. Ved uberettiget unnlatelse av å hente varene, kan Kunden hente varene fra Selgers lager på egen hånd, til et passende tidspunkt (på en arbeidsdag fra kl. 9.00 til 15.00) etter å ha informert Selger om valgt dato. Mulig omlevering av varene fra Selger eller levering til en annen angitt adresse er mulig ved en separat avtale mellom Selger.</p>

<p>8. Selgeren er ansvarlig for varens manglende samsvar med kontrakten som eksisterer på leveringstidspunktet og avslørt innen to år fra dette tidspunktet. Varenes manglende samsvar med kontrakten, som viste seg innen to år fra tidspunktet for levering av varen, skal antas å ha eksistert på leveringstidspunktet, med mindre annet er bevist eller formodningen ikke kan forenes med varens art eller arten av varens manglende samsvar med kontrakten. .</p>

<p>9. Når det gjelder enkelte varer, gir selgeren en tilleggsgaranti på vilkårene gitt i garantidokumentet. Garantien gitt av selgeren utelukker, begrenser eller suspenderer ikke rettighetene til kunden som er en forbruker i henhold til bestemmelsene om ikke-overholdelse av varene med kontrakten.</p>

<p><strong>Gjelder en ikke-forbrukerkunde:</strong></p>

<p>1. Når det gjelder kunder som ikke er forbrukere i henhold til punkt I underseksjon 2 i denne forskriften, i henhold til art. 558 § 1 i Civil Code, utelukker partene Selgers ansvar under garantien for mangler ved den solgte varen.</p>

<p>2. Det eneansvaret under garantien referert til i forrige punkt utelukker ikke selgeren fra å gi en garanti for utvalgte varer eller i løpet av individuelle forhandlinger med en kunde som ikke er en forbruker.</p>

