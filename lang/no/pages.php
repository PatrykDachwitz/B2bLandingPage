<h2>XII. Sluttbestemmelser</h2>

<p>1. Eventuelle tvister som oppstår i forbindelse med gjennomføringen av salgskontrakter mellom selgeren og kunden vil i første omgang bli avgjort gjennom forhandlinger, med den hensikt å løse tvisten i minnelighet.</p>

<p>2. Avgjørelse av eventuelle tvister som oppstår mellom selgeren og kunden som ikke er en forbruker, er underlagt domstolen som har jurisdiksjon over selgerens sete.</p>

<p>3. I saker som ikke dekkes av denne forskriften, skal bestemmelsene i gjeldende lov gjelde, særlig bestemmelsene i lov av 23. april 1964. Civil Code (konsolidert tekst, Journal of Laws av 2014, pkt. 121, med endringer) og lov av 30. mai 1964. 2014 om forbrukerrettigheter, pkt. 014, pkt. 827).</p>

<p>4. Polsk lov skal gjelde for kontrakter inngått med selger. Når det gjelder forbrukerkontrakter, vil de obligatoriske bestemmelsene i landet der forbrukeren bor, bli overholdt, og forbrukeren er beskyttet av disse bestemmelsene uavhengig av valg av polsk lov som gjeldende lov.</p>

