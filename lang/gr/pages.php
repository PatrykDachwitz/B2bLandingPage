<li>στην οποία η τιμή ή η αμοιβή εξαρτάται από τις διακυμάνσεις της χρηματοπιστωτικής αγοράς στις οποίες ο επιχειρηματίας δεν έχει κανέναν έλεγχο και που ενδέχεται να προκύψουν πριν από τη λήξη της προθεσμίας υπαναχώρησης από τη σύμβαση παροχής υπηρεσιών με ακίνητα που καθορίζονται από τον καταναλωτή με τη σειρά που έδωσε ο ίδιος ή συνδέονται στενά με το πρόσωπό του .</li>
<li><strong>στην οποία το αντικείμενο της υπηρεσίας είναι ένα μη προκατασκευασμένο προϊόν, που κατασκευάζεται σύμφωνα με τις προδιαγραφές του καταναλωτή ή εξυπηρετεί την ικανοποίηση των ατομικών του αναγκών.</strong></li>
<li>στην οποία το αντικείμενο της υπηρεσίας είναι αγαθά που αλλοιώνονται γρήγορα ή έχουν μικρή διάρκεια ζωής.</li>
<li>στην οποία το αντικείμενο της υπηρεσίας είναι αγαθά που παραδίδονται σε σφραγισμένη συσκευασία, η οποία δεν μπορεί να επιστραφεί μετά το άνοιγμα της συσκευασίας για λόγους προστασίας της υγείας ή υγιεινής, εάν η συσκευασία ανοίχτηκε μετά την παράδοση.</li>
<li>στην οποία το αντικείμενο της υπηρεσίας είναι αγαθά τα οποία, λόγω της φύσης τους, συνδέονται άρρηκτα με άλλα είδη μετά την παράδοση.</li>
<li>στην οποία το αντικείμενο της υπηρεσίας είναι αλκοολούχα ποτά, η τιμή των οποίων συμφωνήθηκε κατά τη σύναψη της σύμβασης πώλησης και η παράδοση των οποίων μπορεί να πραγματοποιηθεί μόνο μετά από 30 ημέρες και η αξία των οποίων εξαρτάται από τις διακυμάνσεις της αγοράς στις οποίες ο επιχειρηματίας δεν έχει κανέναν έλεγχο.</li>
<li>στην οποία ο καταναλωτής ζήτησε ρητά από τον επιχειρηματία να έρθει σε αυτόν για επείγουσα επισκευή ή συντήρηση. εάν ο επιχειρηματίας παρέχει πρόσθετες υπηρεσίες εκτός από αυτές που ζητούνται από τον καταναλωτή ή προμηθεύει είδη εκτός από ανταλλακτικά που είναι απαραίτητα για την εκτέλεση της επισκευής ή της συντήρησης, ο καταναλωτής έχει το δικαίωμα να υπαναχωρήσει από τη σύμβαση σε σχέση με πρόσθετες υπηρεσίες ή είδη.</li>
<li>στην οποία το αντικείμενο της υπηρεσίας είναι ηχογραφήσεις ή οπτικές ηχογραφήσεις ή προγράμματα υπολογιστή που παραδίδονται σε σφραγισμένη συσκευασία, εάν η συσκευασία ανοίχτηκε μετά την παράδοση.</li>
<li>για την παράδοση εφημερίδων, περιοδικών ή περιοδικών, με εξαίρεση τη σύμβαση συνδρομής.</li>
<li>ολοκληρώθηκε με δημόσια δημοπρασία.</li>
<li>για την παροχή υπηρεσιών διαμονής εκτός από οικιστικούς σκοπούς, μεταφορά αγαθών, ενοικίαση αυτοκινήτου, γαστρονομία, υπηρεσίες που σχετίζονται με αναψυχή, ψυχαγωγία, αθλητικές ή πολιτιστικές εκδηλώσεις, εάν η σύμβαση προσδιορίζει την ημέρα ή την περίοδο παροχής της υπηρεσίας.</li>
<li>για την παράδοση ψηφιακού περιεχομένου που δεν είναι εγγεγραμμένο σε απτό μέσο, εάν η εκτέλεση της υπηρεσίας ξεκίνησε με τη ρητή συγκατάθεση του καταναλωτή πριν από τη λήξη της προθεσμίας υπαναχώρησης από τη σύμβαση και αφού τον ενημέρωσε ο επιχειρηματίας για την απώλεια του δικαιώματος υπαναχώρησης από τη σύμβαση.</li>
