<h2>V. Παράδοση</h2>

<p>1. Η παράδοση των Αγαθών περιορίζεται στην περιοχή της Μεγάλης Βρετανίας και πραγματοποιείται στη διεύθυνση που υποδεικνύει ο Πελάτης κατά την υποβολή της παραγγελίας. Η παράδοση των αγαθών εκτός του ΗΒ πραγματοποιείται μέσω μεμονωμένων συμφωνιών μεταξύ του Πωλητή και του Πελάτη.</p>

<p>2. Η παράδοση των παραγγελθέντων εμπορευμάτων πραγματοποιείται μέσω courier. Για υλικοτεχνικούς λόγους, ο Πωλητής δεν προσφέρει τη δυνατότητα προσωπικής παραλαβής της Παραγγελίας στο επίσημο κατάστημά του ή σε οποιοδήποτε άλλο μέρος.</p>

<p>3. Κατά κανόνα, η παράδοση είναι δωρεάν, ανεξάρτητα από τον τρόπο πληρωμής που έχει επιλέξει ο Πελάτης και τον τόπο παράδοσης. Οποιοδήποτε πρόσθετο κόστος παράδοσης μπορεί να αναφέρεται ως επιλογή κατά την υποβολή της παραγγελίας και θα απαιτηθεί η έγκριση του Πελάτη.</p>

<p>4. Ο χρόνος παράδοσης είναι έως 23 εργάσιμες ημέρες, ο χρόνος παραγωγής έως 11 εργάσιμες ημέρες και ο χρόνος παράδοσης έως 7 εργάσιμες ημέρες. Ο χρόνος παράδοσης υπολογίζεται από την ημερομηνία επιβεβαίωσης της παραγγελίας του Πελάτη από τον Πωλητή.</p>

<p>5. Εάν ο Πελάτης είναι Καταναλωτής, ο Πωλητής φέρει τον κίνδυνο τυχαίας ζημιάς ή απώλειας των Αγαθών κατά τη μεταφορά. Ο Πελάτης θα πρέπει να επιθεωρήσει την αποστολή εγκαίρως και με τον τρόπο που γίνεται αποδεκτός για αποστολές αυτού του τύπου. Εάν τα εμπορεύματα παραδοθούν με εμφανή ζημιά ή απώλειες κατά τη μεταφορά, ο Πελάτης θα πρέπει, εάν είναι δυνατόν, να αναφέρει ένα τέτοιο ελάττωμα στον μεταφορέα (courier) και στον πωλητή το συντομότερο δυνατό και να προβεί σε άλλες ενέργειες που είναι απαραίτητες για τον προσδιορισμό της πιθανής ευθύνης του μεταφορέα, ιδίως επιβεβαιώνοντας την κατάσταση της αποστολής. Η καθυστέρηση στην αναφορά ενός τέτοιου ελαττώματος δεν προκαλεί αρνητικές συνέπειες για τον Πελάτη, ιδιαίτερα δεν επηρεάζει την ευθύνη του Πωλητή για μη συμμόρφωση των αγαθών με τη σύμβαση (σημείο IX των Κανονισμών). Ωστόσο, μια ταχύτερη ειδοποίηση της παρατηρηθείσας ζημίας που προκλήθηκε κατά τη μεταφορά θα επιτρέψει στον Πωλητή να αναγνωρίσει το παράπονο γρηγορότερα και θα επιτρέψει στον Πωλητή να διεκδικήσει ή να διώξει τον μεταφορέα του.

