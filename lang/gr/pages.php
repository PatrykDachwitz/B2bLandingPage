<h2>IX. Παράπονα σχετικά με αγαθά</h2>

<p><strong>Ισχύει για τον Πελάτη που είναι Καταναλωτής:</strong></p>

<p>1. Όλα τα προϊόντα που προσφέρονται στο Κατάστημα είναι ολοκαίνουργια, χωρίς νομικά ελαττώματα και έχουν εισαχθεί νόμιμα στην αγορά του Ηνωμένου Βασιλείου. Ο Πωλητής έχει νομική υποχρέωση να παραδίδει τα αγαθά χωρίς ελαττώματα. Η ευθύνη του Πωλητή για την ποιότητα της υπηρεσίας που προβλέπεται από το νόμο ρυθμίζεται από τις γενικά ισχύουσες διατάξεις σχετικά με τη μη συμμόρφωση των αγαθών με τη σύμβαση, ιδίως το κεφάλαιο 5α του νόμου της 30ής Μαΐου 2014 για τα δικαιώματα των καταναλωτών.</p>

<p>2. Σε περίπτωση μη συμμόρφωσης των αγαθών με τη σύμβαση, ο Πελάτης μπορεί να υποβάλει παράπονο στον Πωλητή με οποιαδήποτε μορφή, ιδίως γραπτώς στην ακόλουθη διεύθυνση: FORAM spółka z ograniczoną odpowiedzialnością sp. κ., ul. Jeziorna 3, 64-200, Karpicko ή μέσω e-mail στην ακόλουθη διεύθυνση: <a href="mailto: b2b@artforma.eu">b2b@artforma.eu</a>. Στην καταγγελία, συνιστάται:</p>

<ul>
<li>παροχή πληροφοριών σχετικά με το θέμα της καταγγελίας, ιδίως τον τύπο και την ημερομηνία του ελαττώματος</li>
<li>ορισμός του αιτήματος σχετικά με τη μέθοδο άρσης του ελαττώματος (αντικατάσταση του εμπορεύματος με νέο, επισκευή του εμπορεύματος και στη συνέχεια μείωση τιμής ή υπαναχώρηση από τη σύμβαση)</li>
<li>παροχή στοιχείων επικοινωνίας του καταγγέλλοντος</li>
</ul>

<p>που θα διευκολύνει και θα επιταχύνει την εξέταση της καταγγελίας από το Κατάστημα. Οι συστάσεις που προσδιορίζονται στην προηγούμενη πρόταση είναι μόνο μη δεσμευτικές κατευθυντήριες γραμμές και σε καμία περίπτωση δεν επηρεάζουν την αποτελεσματικότητα των καταγγελιών που υποβάλλονται χωρίς την παροχή των συνιστώμενων πληροφοριών.</p>

<p>3. Εάν είναι απαραίτητο να εξεταστεί το παράπονο, ο Πελάτης θα παρέχει στον Πωλητή τα αγαθά που αποτελούν το αντικείμενο της καταγγελίας, τα οποία υπόκεινται σε επισκευή ή αντικατάσταση. Ο πωλητής θα παραλάβει τα αγαθά από τον πελάτη με δικά του έξοδα. Εάν τα αγαθά είχαν εγκατασταθεί πριν αποκαλυφθεί η μη συμμόρφωση των αγαθών με τη σύμβαση, ο πωλητής αποσυναρμολογεί τα αγαθά και τα συναρμολογεί εκ νέου μετά την επισκευή ή την αντικατάσταση ή εκτελεί αυτές τις δραστηριότητες με δικά του έξοδα.</p>

<p>4. Προκειμένου να επιταχυνθεί η εξέταση της καταγγελίας, ο Πελάτης μπορεί να επιτρέψει την εξέταση της καταγγελίας εξ αποστάσεως, ιδίως παρέχοντας φωτογραφίες ή ενεργοποιώντας την επιθεώρηση στη λειτουργία τηλεδιάσκεψης. Προκειμένου να αποφευχθεί η ταλαιπωρία, ο Πελάτης μπορεί να χρησιμοποιήσει τη δυνατότητα εξ αποστάσεως εξέτασης της καταγγελίας και στην περίπτωση που είναι δυνατή η παράδοση του προϊόντος, μια τέτοια απόφαση δεν περιορίζει κανένα από τα δικαιώματα του Πελάτη.</p>

<p>5. Ο πωλητής υποχρεούται να απαντήσει στο παράπονο του καταναλωτή εντός 14 ημερών από την παραλαβή του. Σε περίπτωση αιτήματος αντικατάστασης των αγαθών, άρσης των ελαττωμάτων τους ή υποβολή δήλωσης υπαναχώρησης από τη σύμβαση βάσει των διατάξεων περί μη συμμόρφωσης των αγαθών με τη σύμβαση (Κεφάλαιο 5α του νόμου της 30ης Μαΐου 201, ο καταναλωτής υποχρεούται να παρέχει στον καταναλωτή δικαιώματα στον αγοραστή). για επισκευή ή αλλαγή.</p>

<p>6. Εάν ο Πωλητής κρίνει το παράπονο αδικαιολόγητο, τότε τα αγαθά θα παραδοθούν στον Πελάτη μαζί με τις σχετικές πληροφορίες.</p>

<p>7. Σε περίπτωση αδικαιολόγητης αποτυχίας παραλαβής των αγαθών, ο Πελάτης μπορεί να παραλάβει τα αγαθά από την αποθήκη του Πωλητή μόνος του, σε κατάλληλη στιγμή (μια εργάσιμη ημέρα από τις 9 π.μ. έως τις 3 μ.μ.) αφού ενημερώσει τον Πωλητή για την επιλεγμένη ημερομηνία. Πιθανή εκ νέου παράδοση των αγαθών από τον Πωλητή ή παράδοση σε άλλη υποδεικνυόμενη διεύθυνση είναι δυνατή με ξεχωριστή συμφωνία μεταξύ του Πελάτη και του Πελάτη</>.

<p>8. Ο πωλητής ευθύνεται για τη μη συμμόρφωση των αγαθών με τη σύμβαση που υπήρχε κατά τη στιγμή της παράδοσης και αποκαλύφθηκε εντός δύο ετών από εκείνη τη στιγμή. Η έλλειψη συμμόρφωσης των εμπορευμάτων με τη σύμβαση, η οποία έγινε εμφανής εντός δύο ετών από τη στιγμή της παράδοσης των εμπορευμάτων, τεκμαίρεται ότι υπήρχε κατά τη στιγμή της παράδοσης, εκτός εάν αποδεικνύεται το αντίθετο ή το τεκμήριο δεν μπορεί να συμβιβαστεί με τη φύση των αγαθών ή τη φύση της έλλειψης συμμόρφωσης των αγαθών με τη σύμβαση. .</p>

<p>9. Στην περίπτωση ορισμένων Αγαθών, ο Πωλητής παρέχει πρόσθετη εγγύηση με τους όρους που προβλέπονται στο έγγραφο εγγύησης. Η εγγύηση που παρέχεται από τον Πωλητή δεν αποκλείει, περιορίζει ή αναστέλλει τα δικαιώματα του Πελάτη που είναι Καταναλωτής σύμφωνα με τις διατάξεις περί μη συμμόρφωσης των αγαθών με τη σύμβαση.</p>

<p><strong>Ισχύει για πελάτη που δεν είναι καταναλωτής:</strong></p>

<p>1. Στην περίπτωση Πελατών που δεν είναι Καταναλωτές κατά την έννοια του σημείου I εδάφιο 2 του παρόντος Κανονισμού, σύμφωνα με το άρθ. 558 § 1 του Αστικού Κώδικα, τα μέρη αποκλείουν την ευθύνη του Πωλητή βάσει της εγγύησης για ελαττώματα στο πωλούμενο αντικείμενο.</p>

<p>2. Η αποκλειστική ευθύνη βάσει της εγγύησης που αναφέρεται στο προηγούμενο σημείο δεν αποκλείει τον Πωλητή από τη χορήγηση εγγύησης για επιλεγμένα Προϊόντα ή κατά τη διάρκεια μεμονωμένων διαπραγματεύσεων με Πελάτη που δεν είναι Καταναλωτής.</p>

