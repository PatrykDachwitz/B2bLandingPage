<h2>IV. Διαδικασία για τη σύναψη Συμφωνίας Πώλησης</h2>

<p>1. Ο Πωλητής μέσω του Καταστήματος διενεργεί εξ αποστάσεως πωλήσεις αγαθών στην προσφορά του. Ο Πελάτης μπορεί να εξοικειωθεί με τα κύρια χαρακτηριστικά των εμπορευμάτων πριν υποβάλει παραγγελία στις σελίδες του Ηλεκτρονικού Καταστήματος σχετικά με μεμονωμένα προϊόντα. Για να συνάψετε μια συμφωνία πώλησης εξ αποστάσεως μέσω του Ηλεκτρονικού καταστήματος, μεταβείτε στον ιστότοπο του Ηλεκτρονικού καταστήματος και επιλέξτε τα Αγαθά, ακολουθώντας περαιτέρω τεχνικά βήματα με βάση τα μηνύματα που εμφανίζονται στον Πελάτη και τις πληροφορίες που είναι διαθέσιμες στον ιστότοπο.</p>

<p>2. Η επιλογή των παραγγελθέντων Αγαθών από τον Πελάτη γίνεται με την προσθήκη τους στο καλάθι.</p>

<p>3. Κατά την υποβολή της Παραγγελίας - μέχρι να πατηθεί το κουμπί που επιβεβαιώνει την υποβολή της Παραγγελίας - ο Πελάτης έχει τη δυνατότητα να τροποποιήσει τα καταχωρημένα δεδομένα και τα επιλεγμένα Προϊόντα. Για να το κάνετε αυτό, ακολουθήστε τα μηνύματα που εμφανίζονται στον Πελάτη και τις πληροφορίες που είναι διαθέσιμες στον ιστότοπο.</p>

<p>4. Αφού ο Πελάτης παράσχει όλα τα απαραίτητα δεδομένα, θα εμφανιστεί μια περίληψη της παραγγελίας που υποβλήθηκε. Η περίληψη της παραγγελίας που υποβλήθηκε θα περιλαμβάνει, μεταξύ άλλων: περιγραφή επιλεγμένων αγαθών ή υπηρεσιών, τιμές μονάδας αγαθών, συνολική τιμή, πληροφορίες για την έκπτωση που χορηγήθηκε, την αξία της παραγγελίας και όλα τα άλλα έξοδα, συμπεριλαμβανομένων των εξόδων παράδοσης, καθώς και τη διεύθυνση παράδοσης, τα στοιχεία τιμολογίου και τον επιλεγμένο τρόπο πληρωμής.</p>

<p>5. Για την αποστολή Παραγγελίας, είναι απαραίτητο να αποδεχτείτε το περιεχόμενο των Κανονισμών, να παράσχετε προσωπικά δεδομένα που επισημαίνονται ως υποχρεωτικά και να πατήσετε το κουμπί: «Παραγγελία με υποχρέωση πληρωμής». Για να κάνετε μια παραγγελία κάθε φορά απαιτείται προηγούμενη ανάγνωση των Κανονισμών και αποδοχή των όρων τους ως δεσμευτικοί.</p>

<p>6. Μετά την τοποθέτηση της παραγγελίας, ο Πελάτης θα λάβει επιβεβαίωση της σύναψης της εξ αποστάσεως σύμβασης σε ανθεκτικό μέσο στη διεύθυνση ηλεκτρονικού ταχυδρομείου που παρέχεται, η οποία θα περιέχει επίσης το ισχύον περιεχόμενο των Κανονισμών. Η σύμβαση θεωρείται ότι έχει συναφθεί με την παραλαβή της επιβεβαίωσης της παραγγελίας από τον πωλητή.</p>

<p>7. Σε περίπτωση μη διαθεσιμότητας ορισμένων από τα Αγαθά που καλύπτονται από την Παραγγελία ή ο χρόνος παράδοσης υπερβαίνει τις 14 ημέρες, ο Πελάτης θα ενημερωθεί αμέσως σχετικά χωρίς επιβεβαίωση της σύναψης της εξ αποστάσεως σύμβασης και μπορεί να αποφασίσει για τον τρόπο εκπλήρωσης της παραγγελίας (αποδοχή του υποδεικνυόμενου χρόνου παράδοσης, εκτέλεση παραγγελίας σε σχέση με ορισμένα αγαθά, ακύρωση της παραγγελίας συνολικά).</p>

<p>8. Σε περίπτωση απουσίας απόδειξης επιβεβαίωσης της σύναψης σύμβασης εξ αποστάσεως σε σταθερό μέσο στη διεύθυνση ηλεκτρονικού ταχυδρομείου που παρέχεται από τον Πελάτη ή αποτυχίας λήψης πληροφοριών σχετικά με τη μη διαθεσιμότητα ορισμένων από τα Αγαθά εντός 24 ωρών από την τοποθέτηση της παραγγελίας, ο Πελάτης θα πρέπει να επικοινωνήσει αμέσως με τον Πωλητή.</p>

<p>9. Η σύμβαση πώλησης συνάπτεται στα Αγγλικά και αναπόσπαστο μέρος της αποτελούν οι διατάξεις των παρόντων Κανονισμών στο βαθμό που δεν έρχονται σε αντίθεση με την ίδια τη σύμβαση πώλησης.</p>

<p>10. Η καταγραφή, η ασφάλεια και η κοινοποίηση των δεδομένων παραγγελίας και των Κανονισμών πραγματοποιείται με τη μορφή μηνύματος που αποστέλλεται στη διεύθυνση ηλεκτρονικού ταχυδρομείου που παρέχεται από τον Πελάτη. Οι πελάτες που έχουν δημιουργήσει Λογαριασμό στο Ηλεκτρονικό Κατάστημα έχουν επιπλέον πρόσβαση στις προηγούμενες παραγγελίες τους ως μέρος της λειτουργικότητας του Ηλεκτρονικού καταστήματος.</p>

